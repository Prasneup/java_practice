public class Switch_Example {
    public static void main(String[] args){
        int age = 20;

        switch (age){
            case 20:
            System.out.println("He is a adult.");
            break;
            case 40:
            System.out.println("He is a middle-aged");
            break;
            default:
            System.out.println("He is a child");
        }
    }
    
}
