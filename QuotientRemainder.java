public class QuotientRemainder {
    public static void main(String[] args) {
        int num1 = 4;
        int num2 = 3;
        
        int quo = num1 % num2;
        int rem = num1 / num2;

        System.out.println("the quotient is "+quo);
        System.out.println("The remainder is "+rem);

    }
    
}
