public class ParameterAndReturn {
    //No Parameter and No Return Value.
    public static void main(String[] args){
        myName();
        myCompanyInfo();

    }

    public static void myName(){
        System.out.println("My name is Prasanna Neupane.");
    }
    public static void myCompanyInfo(){
        System.out.println("My Company name: 1Billion");
        System.out.println("NetValuation is: 2.2Billion$");
        System.out.println("Located in: USA");
        System.out.println("More than 2000 Employee works there...");
    }
}
